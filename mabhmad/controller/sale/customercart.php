		$results = $this->model_sale_customercart->getCustomers($filter_data);

		foreach ($results as $result) {

			$data['customers'][] = array(
				'customer_id'    => $result['customer_id'],
				'name'           => $result['name'],
				'email'          => $result['email'],
				'customer_group' => $result['customer_group'],
